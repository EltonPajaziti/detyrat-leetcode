/*
	Detyra 8
*/
#include <iostream>

using namespace std;

int main() {

	int a = 2;

	cout << "a: " << a << endl;
	cout << "a++: " << a++ <<endl;
	a = a + 8;
	cout << "a = a + 8: " << a << endl;
	cout << "++a: " << ++a << endl;
	cout << "a--: " << a-- << endl;
	a -= 4;
	cout << "a -= 4: " << a << endl;
	cout << "--a: " << --a << endl;
	cout << "a: " << a << endl;

	return 0;
}